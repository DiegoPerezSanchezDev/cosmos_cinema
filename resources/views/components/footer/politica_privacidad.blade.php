            <h2>5. ¿Con quién compartimos tus datos?</h2>
            <p>No compartiremos tus datos personales con terceros, excepto en los siguientes casos:</p>
            <ul>
                <li><strong>Proveedores de servicios:</strong> Empresas que nos prestan servicios necesarios para nuestra actividad (ej. pasarelas de pago, proveedores de hosting, servicios de envío de email marketing, herramientas de análisis). Estos proveedores solo accederán a tus datos siguiendo nuestras instrucciones y con la obligación de mantener la confidencialidad y seguridad de los mismos.</li>
                <li><strong>Autoridades públicas:</strong> Cuando así nos lo exija la ley, para cumplir con obligaciones legales o responder a requerimientos de juzgados, tribunales o administraciones públicas.</li>
            </ul>
            <p>No vendemos tus datos personales a terceros. Si en el futuro consideráramos realizar otras comunicaciones de datos personales, te informaríamos oportunamente y solicitaríamos tu consentimiento si fuera necesario.</p>
        </section>

        <section class="legal-section">
            <h2>6. ¿Cuánto tiempo guardaremos tus datos?</h2>
            <p>Conservaremos tus datos personales únicamente durante el tiempo necesario para cumplir con las finalidades para las que fueron recogidos, así como para cumplir con nuestras obligaciones legales.</p>
            <ul>
                <li><strong>Datos de compras:</strong> Durante el tiempo necesario para gestionar la compra y posteriormente durante los plazos de prescripción legales aplicables.</li>
                <li><strong>Datos de cuenta de usuario:</strong> Mientras mantengas tu cuenta activa. Si la cuenta permanece inactiva por un periodo prolongado (24-36 meses), podremos proceder a su eliminación previa notificación.</li>
                <li><strong>Datos para comunicaciones comerciales:</strong> Hasta que revoques tu consentimiento.</li>
                <li><strong>Datos de consultas:</strong> Durante el tiempo necesario para gestionar tu consulta y, en su caso, durante los plazos de prescripción legales.</li>
            </ul>
            <p>Una vez que tus datos ya no sean necesarios, los eliminaremos de forma segura o los anonimizaremos.</p>
        </section>

