            <h2>3. ¿Qué datos personales tratamos, para qué y con qué base legal?</h2>
            <p>A continuación, detallamos las finalidades para las que tratamos tus datos personales, los tipos de datos que podemos tratar y la base legal que nos legitima para ello:</p>

            <h3 class="legal-subsection-title">Gestión de compras y servicios:</h3>
            <ul>
                <li><strong>Finalidad:</strong> Procesar la compra de entradas de cine, productos de restauración u otros servicios que ofrezcamos. Esto incluye el envío de confirmaciones, entradas digitales y la gestión de cualquier incidencia relacionada con tu compra.</li>
                <li><strong>Tipología de datos:</strong> Datos identificativos (nombre, apellidos), datos de contacto (email, teléfono), datos económicos (información de tarjeta de pago, gestionada de forma segura a través de nuestros proveedores de servicios de pago), detalles de la compra.</li>
                <li><strong>Base legal:</strong> La ejecución del contrato de compraventa o prestación de servicios que suscribes con nosotros.</li>
            </ul>

            <h3 class="legal-subsection-title">Creación y gestión de cuenta de usuario:</h3>
            <ul>
                <li><strong>Finalidad:</strong> Permitirte crear y gestionar tu cuenta personal en nuestro Sitio Web, acceder a tu historial de compras, guardar preferencias, etc.</li>
                <li><strong>Tipología de datos:</strong> Datos identificativos (nombre, apellidos, nombre de usuario), datos de contacto (email), credenciales de acceso.</li>
                <li><strong>Base legal:</strong> La ejecución del contrato para la prestación del servicio de cuenta de usuario, basado en tu solicitud.</li>
            </ul>

            <h3 class="legal-subsection-title">Comunicaciones comerciales y boletín de noticias:</h3>
            <ul>
                <li><strong>Finalidad:</strong> Enviarte información sobre nuestras películas, eventos especiales, promociones, ofertas y noticias de Cosmos Cinema, siempre que hayas consentido recibirlas.</li>
                <li><strong>Tipología de datos:</strong> Datos identificativos (nombre), datos de contacto (email).</li>
                <li><strong>Base legal:</strong> Tu consentimiento explícito, que puedes retirar en cualquier momento.</li>
            </ul>

            <h3 class="legal-subsection-title">Atención al cliente y gestión de consultas/reclamaciones:</h3>
            <ul>
                <li><strong>Finalidad:</strong> Atender y gestionar tus solicitudes, consultas, quejas o sugerencias.</li>
                <li><strong>Tipología de datos:</strong> Datos identificativos (nombre), datos de contacto (email, teléfono), y cualquier otra información que nos proporciones en tu comunicación.</li>
                <li><strong>Base legal:</strong> Nuestro interés legítimo en ofrecerte un buen servicio al cliente, o el cumplimiento de una obligación legal en caso de reclamaciones formales.</li>
            </ul>

            <h3 class="legal-subsection-title">Participación en concursos y promociones:</h3>
            <ul>
                <li><strong>Finalidad:</strong> Gestionar tu participación en concursos, sorteos o promociones que organicemos.</li>
                <li><strong>Tipología de datos:</strong> Datos identificativos (nombre, apellidos), datos de contacto (email, teléfono), y datos específicos requeridos por las bases del concurso/promoción.</li>
                <li><strong>Base legal:</strong> La ejecución del contrato basado en la aceptación de las bases legales del concurso o promoción. Tu consentimiento para la publicación de ganadores si aplica.</li>
            </ul>

            <h3 class="legal-subsection-title">Mejora de nuestros servicios y análisis:</h3>
            <ul>
                <li><strong>Finalidad:</strong> Realizar análisis estadísticos sobre el uso de nuestro Sitio Web y la acogida de nuestros productos y servicios para mejorar nuestra oferta y la experiencia del usuario.</li>
                <li><strong>Tipología de datos:</strong> Datos de navegación (páginas visitadas, tiempo de permanencia), datos técnicos (dirección IP anonimizada, tipo de dispositivo, navegador).</li>
                <li><strong>Base legal:</strong> Nuestro interés legítimo en mejorar nuestros servicios, o tu consentimiento para el uso de cookies analíticas.</li>
            </ul>

            <h3 class="legal-subsection-title">Cumplimiento de obligaciones legales:</h3>
            <ul>
                <li><strong>Finalidad:</strong> Cumplir con las obligaciones legales que nos sean aplicables (fiscales, mercantiles, etc.).</li>
                <li><strong>Tipología de datos:</strong> Los datos personales necesarios para cumplir con dichas obligaciones.</li>
                <li><strong>Base legal:</strong> El cumplimiento de una obligación legal.</li>
            </ul>
