            <p>Mediante el acceso al Sitio Web y la aceptación a través del panel de configuración de cookies, acepta de manera expresa la utilización de este tipo de Cookies en sus dispositivos, según las preferencias que haya establecido. Si desactiva las Cookies, puede que su navegación por el Sitio Web no sea óptima y algunas de las utilidades de que dispone el Sitio Web no funcionen correctamente.</p>
            <p>Concretamente, el Prestador está utilizando las Cookies para las finalidades que a continuación se exponen. Si en un futuro el Prestador utilizase otras con el propósito de otorgar más y mejores servicios, se informará al usuario de ello y se solicitará su consentimiento si fuera necesario.</p>

            <h3 class="legal-subsection-title">Tipos de Cookies que utilizamos:</h3>
            <ul>
                <li>
                    <strong>Cookies estrictamente necesarias:</strong>
                    <p>Son aquellas cookies consideradas imprescindibles para la navegación por el Sitio Web, pues facilitan al usuario la utilización de sus prestaciones o herramientas como, por ejemplo, identificar la sesión, acceder a partes de acceso restringido, recordar los elementos que integran un pedido, realizar el proceso de compra de un pedido, entre otros. Sin estas cookies, las funciones mencionadas quedarían inhabilitadas.</p>
                </li>
                <li>
                    <strong>Cookies de sesión:</strong>
                    <p>Las cookies de sesión son memorizadas y únicamente tienen validez temporal, es decir, hasta que el Usuario finaliza la navegación por el Sitio Web. Estas cookies no graban de forma permanente ninguna información en el disco duro de su ordenador.</p>
                </li>
                <li>
                    <strong>Cookies de navegación o funcionales:</strong>
                    <p>El objetivo principal de estas cookies es mejorar la experiencia del usuario, por ejemplo, recordando preferencias (idioma, cine favorito) y evitar ofrecer recomendaciones no afines a sus intereses. Estas cookies basan su utilidad en el seguimiento temporal de la navegación por Internet. El Usuario tiene la posibilidad de gestionar estas cookies a través de nuestro panel de configuración.</p>
                </li>
                <li>
                    <strong>Cookies estadísticas o de rendimiento:</strong>
                    <p>Estas cookies permiten conocer cómo interactúan los usuarios con el Sitio Web, por ejemplo: la fecha y la hora de la última vez que el Usuario visitó el Sitio Web, el acceso a los contenidos que el Usuario escogió en su última visita. Para realizar las estadísticas de uso del Sitio Web utilizamos herramientas estadísticas con el fin de conocer el nivel de recurrencia de nuestros visitantes y los contenidos que resultan más interesantes. De esta manera podemos concentrar nuestros esfuerzos en mejorar las áreas más visitadas y hacer que el Usuario encuentre más fácilmente lo que busca. La información recopilada es generalmente agregada y anónima.</p>
                </li>
                <li>
                    <strong>Cookies analíticas:</strong>
                    <p>Son aquellas Cookies utilizadas con el propósito de analizar el uso que hacen los usuarios del Sitio Web. La información que genera la Cookie (pudiendo incluir parte de su dirección IP) puede ser transmitida y archivada por proveedores de estos servicios. Se usará esta información por cuenta del Prestador con el propósito de seguir la traza de su uso del Sitio Web, recopilando la actividad del Sitio Web y prestando otros servicios relacionados con la actividad del Sitio Web y el uso de Internet.</p>
                </li>
                <li>
                    <strong>Cookies publicitarias y de personalización:</strong>
                    <p>Son aquellas Cookies utilizadas por el Prestador o por terceros para gestionar eficazmente los espacios publicitarios del Sitio Web, adecuando el contenido del anuncio al contenido del servicio solicitado o al uso que realice de nuestra página web. Así podemos analizar sus hábitos de navegación en Internet y podemos mostrarle publicidad relacionada con su perfil de navegación.</p>
                </li>
                <li>
                    <strong>Cookies de Redes Sociales:</strong>
                    <p>Son aquellas Cookies utilizadas en modo de conectores y aplicaciones de las redes sociales para que el usuario se pueda identificar fácilmente y/o para que pueda interactuar con el Sitio Web de manera más sencilla (ej. compartir contenido). Para ello, podríamos necesitar instalar Cookies de cada una de las redes sociales, sujeto a su consentimiento y a las políticas de privacidad de dichas redes.</p>
                </li>
                <li>
                    <strong>Cookies de terceros:</strong>
                    <p>En determinadas ocasiones podemos ofrecer a través del Sitio Web contenidos o funcionalidades proporcionadas por terceros (ej. reproductores de vídeo, mapas). Estos terceros pueden instalar cookies que escapan de nuestro control. El uso de estas cookies se rige por las políticas de privacidad de dichos terceros.</p>
                </li>
            </ul>
