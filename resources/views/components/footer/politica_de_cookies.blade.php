            <h2>¿Qué es una Cookie?</h2>
            <p>Las cookies son pequeños ficheros de datos que se generan en el ordenador del Usuario y que permiten que sea el propio Usuario el que almacene la información que genera su actividad en la red. Su finalidad principal es reconocer al usuario cada vez que accede al Sitio Web permitiendo, además, mejorar la calidad y ofrecer un mejor uso del Sitio Web.</p>
            <p>Las cookies permiten, entre otras cosas, almacenar y recuperar información sobre los hábitos de navegación de un Usuario o de su equipo y, dependiendo de la información que contengan y de la forma en que utilice su equipo, pueden utilizarse para reconocer al Usuario.</p>
            <p>Las Cookies son esenciales para el funcionamiento de Internet; no dañan el equipo/dispositivo del usuario y, si se encuentran activadas en la configuración de su navegador, ayudan a identificar y resolver posibles errores de funcionamiento del Sitio Web.</p>
            <p>Al utilizar este Sitio Web, y sujeto a su consentimiento gestionado a través de nuestro banner o panel de configuración de cookies, consiente el tratamiento de su información por el Prestador en la forma y para los fines indicados.</p>
